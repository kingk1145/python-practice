# list methods
# 1. append()
friends=["Apple","Banana","Cherry","Date","Elderberry","Fig","Grape"]
print(friends)
friends.append("harry")
print(friends)

# 2. sort
num=[8,3,93,2,4,3,0.2]
print(num)
num.sort()
print(num)


# 3. reverse
hydra=[83,0,4,3,4,47,3,1,45,6,86,56,67,656,]
print(hydra)
hydra.reverse()
print(hydra)

# 4. insert
element=["Wind","Earth","Fire"]
print(element)
element.insert(2,"Lightning")
print(element)

# 5. pop
perodic_element=["Hydrogen","Helium","Lithium","Beryllium","Boron","Carbon"] 
perodic_element.pop(2)
print(perodic_element) 

# 6. remove
number=[1,2,3,4,5,6,7,8,9]
print(number)
number.remove(6)
print(number)
